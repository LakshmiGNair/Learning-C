#include <stdio.h>
int main()
{
  printf("lakshmi\ngnair\n"); // printing the string "lakshmi" followed by a newline character and then "gnair"
  printf("lakshmi\tgnair\n"); // printing the string "lakshmi" followed by a tab character and then "gnair"
  printf("lakshmi\bgnair\n"); // printing the string "lakshmi" followed by a backspace character and then "gnair"
  printf("lakshmi\"gnair\"");
  return 0;
}